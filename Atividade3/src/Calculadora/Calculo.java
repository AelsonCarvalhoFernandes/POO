package Calculadora;

public class Calculo {
	
	double val = 0;
	
	public float somar(float n1, float n2) {
		return n1 + n2;
	}
	
	public float subtrair(float n1, float n2) {
		return n1 - n2;
	}
	
	public float multiplicar(float n1, float n2) {
		return n1*n2;
	}
	
	public float dividir(float n1, float n2) {
		return n1/n2;
	}

}
